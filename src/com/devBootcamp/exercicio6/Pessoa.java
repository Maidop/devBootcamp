package com.devBootcamp.exercicio6;

public class Pessoa {
    //Crie uma nova classe chamada Pessoa, adicionar os atributos nome e cpf, que devem ser obrigat√≥rios,
    private String nome;
    private String cpf;

    public Pessoa(String nome, String cpf) {
        this.nome = nome;
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
}
